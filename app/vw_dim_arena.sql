CREATE VIEW vw_dim_arena AS(
SELECT 
    T.id
    ,T.full_name
    ,TD.arena
    ,TD.city
    ,TD.arenacapacity
FROM team T
LEFT JOIN team_details TD ON T.id = TD.team_id
)    